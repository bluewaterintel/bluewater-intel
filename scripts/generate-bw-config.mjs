#!/usr/bin/env node
/**
 * Generate bw-config.js from environment variables or .env file
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const envPath = join(root, '.env');

function loadEnv() {
  const env = { ...process.env };
  if (existsSync(envPath)) {
    for (const line of readFileSync(envPath, 'utf8').split('\n')) {
      const m = line.match(/^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)\s*$/);
      if (!m || line.trimStart().startsWith('#')) continue;
      env[m[1]] = m[2].replace(/^["']|["']$/g, '');
    }
  }
  return env;
}

const env = loadEnv();
const url = env.SUPABASE_URL || env.VITE_SUPABASE_URL || '';
const anon = env.SUPABASE_ANON_KEY || env.VITE_SUPABASE_ANON_KEY || '';

if (!url || !anon || url.includes('YOURPROJECT') || anon.includes('YOUR_ANON')) {
  console.warn('Warning: SUPABASE_URL or SUPABASE_ANON_KEY not set — bw-config.js will use placeholders.');
}

const out = `// Auto-generated by scripts/generate-bw-config.mjs — do not edit manually
window.BW_DATA_CONFIG = {
  supabaseUrl: ${JSON.stringify(url || 'https://YOURPROJECT.supabase.co')},
  supabaseAnonKey: ${JSON.stringify(anon || 'YOUR_ANON_KEY')},
  embeddedFallback: false,
  queryTimeoutMs: 8000,
};
`;

writeFileSync(join(root, 'bw-config.js'), out);
console.log('Generated bw-config.js');
